import turtle

def func():
    turtle.pensize(1) # 画笔粗细
    turtle.pencolor('red') # 画笔颜色
    turtle.speed(1)
    
    turtle.forward(100)
    turtle.right(90)
    turtle.forward(100)
    turtle.right(90)
    turtle.forward(100)
    turtle.right(90)
    turtle.forward(100)

    turtle.mainloop()